// for loops 

console.log("This is for loop")
for(let i = 1; i<=8;i++){
    console.log(`the value is : ${i}`);

}

// while loops

console.log("This is while loop")
let i = 1;
while(i<=8){
    console.log(`the value is : ${i}`)
    i++;
}

//do while loops

console.log("This is do while loop")
let a = 1;
do{
    console.log(`the value is : ${a}`);
    a++;

}while(a<=8);

